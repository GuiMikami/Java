/*Faça um programa que mostre todos os números 
inteiros de 1 a 50. Use o laço WHILE.
 */
package ExsEstruturasRepeticao;

/**
 *
 * @author GuiMikami
 */
public class Ex01 {

    public static void main(String[] args) {
        int x = 0;

        while (x <= 50) {
            System.out.println(x);
            x++;
        }
    }

}
